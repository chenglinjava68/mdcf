package com.mindai.cf.scheduler.center.rabbit.model;

public class Foo {

	private String name;
	private int age;

	public Foo() {

	}

	public Foo(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
